function playmp3(){
    var browser =window.navigator.userAgent.toLowerCase();
    if(browser.indexOf("ie")>=0){
        var embed =document.embedPlay;
        embed.volume=100;
        embed.play();
    }else{
        var audio =document.getElementById("audioPlay");
        audio.play();

    }
};
